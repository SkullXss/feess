# FEESS - Fast and Efficient Site Scraper

## Descrição

O FEESS (Fast and Efficient Site Scraper) é um script em Bash projetado para baixar o conteúdo de um site de maneira eficiente, organizando os arquivos em uma pasta com o nome do site.

## Requisitos

- Termux (ou ambiente Linux similar)
- wget

## Uso

1. Clone este repositório:

    ```bash
    git clone https://github.com/seu-usuario/feess.git
    ```

2. Navegue até o diretório do script:

    ```bash
    cd feess
    ```

3. Execute o script:

    ```bash
    bash feess.sh
    ```

4. Siga as instruções para inserir a URL do site.

## Funcionalidades

- Banner animado "FEESS - Fast and Efficient Site Scraper"
- Instalação automática de dependências (wget)
- Organização dos arquivos em uma pasta com o nome do site
- Limpeza do conteúdo anterior antes de baixar

